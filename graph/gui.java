    private void jComboBox3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox3ActionPerformed
        if (!jTextField4.getText().equals("")) {
            try {
                jTextArea1.setFont(new Font("Monospaced", Font.PLAIN, Integer.parseInt(jTextField4.getText())));
            } catch (Exception e) {
                //Probs number format exception
            }
        }
        if (points.size() > 0) {
            jTextArea1.setText("");
            String text = (String) jComboBox3.getSelectedItem();
            Point a = Main.original(points.get(0));
            Point b = Main.original(points.get(1));
            Point c = Main.original(points.get(2));
            Point x = null;
            Point y = null;
            String p1name = "";
            String p2name = "";
            if (text.equalsIgnoreCase("XY") || text.equalsIgnoreCase("XZ") || text.equalsIgnoreCase("YZ")) {
                if (text.equals("XY")) {
                    p1name = "X";
                    p2name = "Y";
                    x = a;
                    y = b;
                } else if (text.equals("XZ")) {
                    p1name = "X";
                    p2name = "Z";
                    x = a;
                    y = c;
                } else if (text.equals("YZ")) {
                    p1name = "Y";
                    p2name = "Z";
                    x = b;
                    y = c;
                }
                jTextArea1.append("Solution :\n");
                jTextArea1.append("Set up the distance formula.\n");
                jTextArea1.append("√((x₁-x₂)²+(y₁-y₂)²)\n");
                jTextArea1.append("Substitute variables :\n");
                jTextArea1.append(p1name + "(" + x.x + ", " + x.y + ") and " + p2name + "(" + y.x + ", " + y.y + ") are our points to plug in.\n");
                jTextArea1.append("Let us assume x₁ is " + x.x + ", x₂ is " + y.x + ", y₁ is " + x.y + ", and y₂ is " + y.y + ".\n");
                jTextArea1.append("Plug in the variables into the formula.\n");
                jTextArea1.append("√((" + x.x + "-" + y.x + ")²+(" + x.y + "-" + y.y + ")²)\n");
                jTextArea1.append("Simplify.\n");
                jTextArea1.append("√((" + (x.x - y.x) + ")²+(" + (x.y - y.y) + ")²)\n");
                float squared = (float) Math.pow(x.x - y.x, 2);
                float squared2 = (float) Math.pow(x.y - y.y, 2);
                jTextArea1.append("√(" + squared + "+" + squared2 + "))\n");
                jTextArea1.append("√(" + (squared + squared2) + ")\n");
                jTextArea1.append("Result:\n");
                jTextArea1.append(text + "=" + Util.distance(x, y, round()) + (round() ? " (Result is rounded)" : ""));
            } else if (text.equals("<X") || text.equals("<Y") || text.equals("<Z")) {
                double prime = 0;
                double beta = 0;
                double gamma = 0;
                String name1 = "";
                String name2 = "";
                String name3 = "";
                if (text.equals("<X")) {
                    prime = Util.yz();
                    name1 = "YZ";
                    beta = Util.xy();
                    name2 = "XY";
                    gamma = Util.xz();
                    name3 = "XZ";
                } else if (text.equals("<Y")) {
                    prime = Util.xz();
                    name1 = "XZ";
                    beta = Util.yz();
                    name2 = "YZ";
                    gamma = Util.xy();
                    name3 = "XY";
                } else if (text.equals("<Z")) {
                    prime = Util.xy();
                    name1 = "XY";
                    beta = Util.xz();
                    name2 = "XZ";
                    gamma = Util.yz();
                    name3 = "YZ";
                }
                double t1 = Util.truncate(prime);
                double t2 = Util.truncate(beta);
                double t3 = Util.truncate(gamma);
                jTextArea1.append("Solution : (Numbers may appear truncated during solution but final solution uses untruncated numbers to produce result)\n");
                jTextArea1.append("Set up the law of cosines.\n");
                jTextArea1.append("a²=b²+c²-2bc×Cos(A)\n");
                jTextArea1.append("Set variables. \n");
                jTextArea1.append("a=" + t1 + "(" + name1 + "), b=" + t2 + "(" + name2 + "), and c=" + t3 + "(" + name3 + ").\n");
                jTextArea1.append("Substitute in variables.\n");
                jTextArea1.append("(" + t1 + ")²=(" + t2 + ")²+(" + t3 + ")²-2×" + t2 + "×" + t3 + "×Cos(A)\n");
                jTextArea1.append("Simplify.\n");
                double powed1 = Util.truncate(Util.square(t1));
                double powed2 = Util.truncate(Util.square(t2));
                double powed3 = Util.truncate(Util.square(t3));
                jTextArea1.append(powed1 + "=" + powed2 + "+" + powed3 + "+(" + (-2 * t2 * t3) + "×Cos(A))\n");
                jTextArea1.append(powed1 + "=" + (powed2 + powed3) + "+(" + (-2 * t2 * t3) + "×Cos(A))\n");
                jTextArea1.append(Util.truncate(powed1 - (powed2 + powed3)) + "=" + (-2 * t2 * t3) + "×Cos(A)\n");
                jTextArea1.append("(" + Util.truncate(powed1 - (powed2 + powed3)) + ")/(" + (-2 * t2 * t3) + ")=Cos(A)\n");
                jTextArea1.append("m" + text + "=arccos(" + ((Util.truncate(powed1 - (powed2 + powed3)) / (-2 * t2 * t3))) + ")\n");
                jTextArea1.append("Result:\n");
                jTextArea1.append("m" + text + "=" + Util.angle(prime, beta, gamma, round()) + "°" + (round() ? " (Result is rounded)" : ""));
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please plot a graph first.");
        }
    }//GEN-LAST:event_jComboBox3ActionPerformed

