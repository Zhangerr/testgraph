    private void jCheckBox5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBox5ActionPerformed
        if (points.size() > 0) {
            int orig = Main.getScale();
            main:
            for (int i = Main.possible.size() - 1; i >= 0; i--) {
                int scalee = Main.possible.get(i);
                //   System.out.println("Scale " + scalee);
                for (int j = 0; j < 3; j++) {
                    //System.out.println(j);
                    if (!Main.m.pointOnScreen(points.get(j), scalee)) {
                        //     System.out.println("failed");
                        if (i != 0) { //last
                            continue main;

                        }
                    }
                }

                Main.setScale(scalee);
                // System.out.println(Main.getScale());
                Main.setGridRepaint(true);
                Main.refreshPointsToScale(orig);
                jComboBox1.setSelectedIndex(i);
                break;
            }
            if (evt != null) {
                Main.m.repaint();

            }
        }
    }//GEN-LAST:event_jCheckBox5ActionPerformed

